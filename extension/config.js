// Найти подразделение и роль по username
function resolveOfficeAndRole(username) {
  for (const [officeId, office] of Object.entries(OFFICES)) {
    for (const [role, users] of Object.entries(office.roles)) {
      if (users.includes(username)) return { officeId, role, officeName: office.name };
    }
  }
  return null;
}

// Можно ли fromOffice ставить задачи toOffice
function canAssignTask(fromOffice, toOffice) {
  if (fromOffice === toOffice) return true;
  if (OFFICES[fromOffice]?.isHQ)  return true;
  return false;
}

